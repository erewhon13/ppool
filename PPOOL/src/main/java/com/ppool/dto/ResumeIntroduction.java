package com.ppool.dto;

public class ResumeIntroduction {

	private int userNo;
	private String resumeIntroductionNo;
	private String resumeIntroduction;


	public String getResumeIntroductionNo() {
		return resumeIntroductionNo;
	}
	public void setResumeIntroductionNo(String resumeIntroductionNo) {
		this.resumeIntroductionNo = resumeIntroductionNo;
	}
	public int getUserNo() {
		return userNo;
	}
	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}
	public String getResumeIntroduction() {
		return resumeIntroduction;
	}
	public void setResumeIntroduction(String resumeIntroduction) {
		this.resumeIntroduction = resumeIntroduction;
	}
	
	
	
}
