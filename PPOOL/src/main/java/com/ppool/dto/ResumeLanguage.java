package com.ppool.dto;

public class ResumeLanguage {


	private int userNo;
	private String resumeLanguageNo;
	private String resumeLanguage;
	private int resumeLanguageGrade;
	
	
	public String getResumeLanguageNo() {
		return resumeLanguageNo;
	}
	public void setResumeLanguageNo(String resumeLanguageNo) {
		this.resumeLanguageNo = resumeLanguageNo;
	}
	public int getUserNo() {
		return userNo;
	}
	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}
	public String getResumeLanguage() {
		return resumeLanguage;
	}
	public void setResumeLanguage(String resumeLanguage) {
		this.resumeLanguage = resumeLanguage;
	}
	public int getResumeLanguageGrade() {
		return resumeLanguageGrade;
	}
	public void setResumeLanguageGrade(int resumeLanguageGrade) {
		this.resumeLanguageGrade = resumeLanguageGrade;
	}
	
}
